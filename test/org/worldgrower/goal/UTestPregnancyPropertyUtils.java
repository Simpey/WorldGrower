package org.worldgrower.goal;

import static org.junit.Assert.assertEquals;

import org.junit.Test;
import org.worldgrower.Constants;
import org.worldgrower.TestUtils;
import org.worldgrower.WorldObject;
import org.worldgrower.attribute.WorldObjectContainer;

public class UTestPregnancyPropertyUtils {

	@Test
	public void testMakePregnant() {
		WorldObject performer = createPerformer();
		assertEquals(null, performer.getProperty(Constants.PREGNANCY));
		
		PregnancyPropertyUtils.makePregnant(performer);
		assertEquals(0, performer.getProperty(Constants.PREGNANCY).intValue());
		
		performer.setProperty(Constants.PREGNANCY, 1000);
		PregnancyPropertyUtils.makePregnant(performer);
		assertEquals(1000, performer.getProperty(Constants.PREGNANCY).intValue());
	}
	
	private WorldObject createPerformer() {
		WorldObject performer = TestUtils.createSkilledWorldObject(1, Constants.INVENTORY, new WorldObjectContainer());
		performer.setProperty(Constants.X, 0);
		performer.setProperty(Constants.Y, 0);
		performer.setProperty(Constants.WIDTH, 1);
		performer.setProperty(Constants.HEIGHT, 1);
		return performer;
	}
}